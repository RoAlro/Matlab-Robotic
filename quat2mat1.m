function R=quat2mat1(Q)
R=2*[Q(1,1)^2+Q(1,2)^2-1/2 Q(1,2)*Q(1,3)-Q(1,4)*Q(1,1) Q(1,2)*Q(1,4)+Q(1,3)*Q(1,1) 0;
    Q(1,1)*Q(1,2)+Q(1,4)*Q(1,1) Q(1,1)^2+Q(1,3)^2-1/2 Q(1,3)*Q(1,4)-Q(1,2)*Q(1,1) 0;
    Q(1,2)*Q(1,4)-Q(1,3)*Q(1,1) Q(1,3)*Q(1,4)+Q(1,2)*Q(1,1) Q(1,1)^2+Q(1,4)^2-1/2 0;
    0 0 0 1/2];

end